#include <iostream>
using namespace std;

int main()
{
    // Deklarasi Variabel
    int nilai, hasil;

    nilai = 1;
    cout << "Input: " << nilai << endl;
    hasil = nilai % 2;

    cout << "Proses: " << nilai << "%2 = " << endl;
    cout << "Hasil: " << hasil << endl << endl;
    // ==============================================
    nilai = 2;
    cout << "Input: " << nilai << endl;
    hasil = nilai % 2;

    cout << "Proses: " << nilai << "%2 = " << endl;
    cout << "Hasil: " << hasil << endl << endl;
    // ==============================================
    nilai = 3;
    cout << "Input: " << nilai << endl;
    hasil = nilai % 2;

    cout << "Proses: " << nilai << "%2 = " << endl;
    cout << "Hasil: " << hasil << endl << endl;
    // ==============================================
    nilai = 4;
    cout << "Input: " << nilai << endl;
    hasil = nilai % 2;

    cout << "Proses: " << nilai << "%2 = " << endl;
    cout << "Hasil: " << hasil << endl << endl;
}